/** Wall-clock seconds in one minute. */
export const SECONDS_PER_MINUTE = 60;

/** Wall-clock seconds in one hour. */
export const SECONDS_PER_HOUR = 60 * SECONDS_PER_MINUTE;

/** Wall-clock seconds in one day (24 hours). */
export const SECONDS_PER_DAY = 24 * SECONDS_PER_HOUR;

/** Wall-clock seconds in one week (7 days). */
export const SECONDS_PER_WEEK = 7 * SECONDS_PER_DAY;

/** Wall-clock seconds in one calendar year (365 days). */
export const SECONDS_PER_YEAR = 365 * SECONDS_PER_DAY;
